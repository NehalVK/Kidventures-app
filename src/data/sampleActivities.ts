// Helper function to get random samples
export const getRandomSamples = (category: string, count: number = 10) => {
  const samples = sampleActivities[category as keyof typeof sampleActivities];
  if (!samples) return [];
  
  const shuffled = [...samples].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
